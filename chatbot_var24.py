def clear_phrase(phrase: str) -> str:
    """
    Очистка фразы: нижний регистр, замена ё→е, удаление лишних символов.
    Реализует этап spell correction (подготовка к Левенштейну).
    """
    phrase = phrase.lower().strip()
    phrase = re.sub(r'[ёЁ]', 'е', phrase)  # нормализация ё
    phrase = re.sub(r'\s+', ' ', phrase)   # множественные пробелы
    # Кириллица + латиница + цифры (нужны для названий моделей: iPhone 17, S26, Redmi Note 14)
    alphabet = ('абвгдежзийклмнопрстуфхцчшщъыьэюя'
                'abcdefghijklmnopqrstuvwxyz'
                '0123456789- ')
    return ''.join(c for c in phrase if c in alphabet).strip()


def lemmatize_phrase(phrase: str) -> str:
    """
    Лемматизация с помощью библиотеки Natasha.
    При отсутствии Natasha — возвращает оригинал (graceful degradation).
    """
    try:
        from natasha import (
            Segmenter, MorphVocab, NewsEmbedding,
            NewsMorphTagger, Doc
        )
        segmenter = Segmenter()
        emb = NewsEmbedding()
        morph_tagger = NewsMorphTagger(emb)
        morph_vocab = MorphVocab()
        doc = Doc(phrase)
        doc.segment(segmenter)
        doc.tag_morph(morph_tagger)
        for token in doc.tokens:
            token.lemmatize(morph_vocab)
        return ' '.join(t.lemma for t in doc.tokens)
    except ImportError:
        return phrase  # Natasha не установлена
    except Exception:
        return phrase


def get_sentiment(phrase: str) -> float:
    """
    Анализ тональности фразы.
    Возвращает число от -1 (негатив) до +1 (позитив).
    """
    words = clear_phrase(phrase).split()
    scores = [SENTIMENT_DICT.get(w, 0.0) for w in words]
    return sum(scores) / len(scores) if scores else 0.0


