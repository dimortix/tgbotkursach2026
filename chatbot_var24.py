# ═══════════════════════════════════════════════════════════════════

def get_answer_by_intent(intent: str):
    """Возвращает случайный ответ из словаря намерений."""
    data = BOT_CONFIG['intents'].get(intent)
    if data and data.get('responses'):
        return random.choice(data['responses'])
    return None


def generate_answer(replica: str):
    """
    Генерирует ответ из датасета диалогов (dialogues.txt).
    Использует расстояние Левенштейна для поиска похожего вопроса.
    """
    if not dialogues_structured_cut:
        return None
    replica_clean = clear_phrase(replica)
    words = set(replica_clean.split())

    # Собираем мини-датасет из диалогов по ключевым словам
    mini_dataset = []
    for word in words:
        mini_dataset.extend(dialogues_structured_cut.get(word, []))

    # Удаляем дубликаты
    seen, unique = set(), []
    for item in mini_dataset:
        if item[0] not in seen:
            seen.add(item[0])
            unique.append(item)

    # Ищем наиболее похожий вопрос (взвешенное расстояние Левенштейна)
    candidates = []
    for q, a in unique:
        if not q:
            continue
        length_ratio = abs(len(replica_clean) - len(q)) / len(q)
        if length_ratio < 0.2:
            dist = nltk.edit_distance(replica_clean, q)
            dist_weighted = dist / len(q)
            if dist_weighted < 0.2:
                candidates.append((dist_weighted, a))

    return min(candidates, key=lambda x: x[0])[1] if candidates else None

