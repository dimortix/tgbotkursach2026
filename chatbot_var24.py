# РАЗДЕЛ 3: ML — ОБУЧЕНИЕ КЛАССИФИКАТОРА НАМЕРЕНИЙ (TF-IDF + SVM)
# ═══════════════════════════════════════════════════════════════════
def train_classifier():
    """Обучает классификатор намерений на датасете из BOT_CONFIG."""
    X_text, y = [], []
    for intent, data in BOT_CONFIG['intents'].items():
        for example in data['examples']:
            X_text.append(example)
            y.append(intent)
    # TF-IDF на уровне символьных n-грамм (2-3) — устойчив к опечаткам
    vec = TfidfVectorizer(analyzer='char', ngram_range=(2, 3))
    X = vec.fit_transform(X_text)
    clf = LinearSVC(max_iter=3000)
    clf.fit(X, y)
    logger.info(f'Классификатор обучен на {len(X_text)} примерах.')
    return vec, clf

vectorizer, clf = train_classifier()

