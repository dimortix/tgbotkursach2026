def tg_handle_voice(update, context):
    """
    Обработчик голосовых сообщений.
    OGG → WAV → Speech-to-Text → bot() → Text-to-Speech → reply.
    """
    user_id = update.effective_user.id
    ogg_path = wav_path = None
    try:
        # Шаг 1: скачиваем голосовое сообщение (с увеличенным таймаутом)
        try:
            voice_file = context.bot.get_file(
                update.message.voice.file_id, timeout=60
            )
            with tempfile.NamedTemporaryFile(suffix='.ogg', delete=False) as f_ogg:
                ogg_path = f_ogg.name
            voice_file.download(ogg_path, timeout=60)
        except Exception as e:
            logger.error(f'[Голос] Шаг 1 (скачивание из Telegram): {e}')
            update.message.reply_text(
                '😔 Не удалось скачать голосовое (таймаут сети). Попробуйте ещё раз.'
            )
            return

        # Шаг 2: конвертация OGG → WAV (требует ffmpeg)
        wav_path = ogg_path.replace('.ogg', '.wav')
        ret = os.system(f'ffmpeg -y -loglevel quiet -i "{ogg_path}" "{wav_path}"')
        if ret != 0:
            logger.error(f'[Голос] Шаг 2 (ffmpeg): код возврата {ret}')
            update.message.reply_text(
                '⚠️ Не удалось обработать аудио (ffmpeg). Напишите текстом!'
            )
            return

        # Шаг 3: распознавание речи (Google STT)
        text = speech_to_text(wav_path)
        if not text:
            update.message.reply_text(
                '🎤 Не расслышал. Попробуйте ещё раз или напишите текстом.'
            )
            return

        # Шаг 4: ответ бота + текстовый ответ с распознанной фразой
        hist_theme = _get_hist(user_id)
        answer = bot(text, hist_theme, user_id)
        update.message.reply_text(
            f'🎤 Вы сказали: «{text}»\n\n{answer}', timeout=60
        )

        # Шаг 5: голосовой ответ (Text-to-Speech), не критичен
        try:
            audio_path = text_to_speech(answer)
            if audio_path:
                with open(audio_path, 'rb') as audio_file:
                    update.message.reply_voice(audio_file, timeout=60)
                os.unlink(audio_path)
        except Exception as e:
            logger.error(f'[Голос] Шаг 5 (TTS/отправка озвучки): {e}')

    except Exception as e:
        logger.error(f'[Голос] Непредвиденная ошибка: {e}')
        update.message.reply_text('😔 Ошибка обработки голоса. Напишите текстом!')
    finally:
        # Очистка временных файлов в любом случае
        for path in (ogg_path, wav_path):
            if path and os.path.exists(path):
                try:
                    os.unlink(path)
                except OSError:
                    pass


def start_health_server():
    """
    Запускает простой HTTP-сервер в фоновом потоке.
    Нужен для хостингов (Render, Railway и т.п.), которые требуют,
    чтобы Web Service слушал порт из переменной окружения PORT.
    Без этого сервис будет считаться «упавшим» и перезапускаться.
    """
    port = os.environ.get('PORT')
    if not port:
        return  # PORT не задан (локальный запуск) — health-сервер не нужен
    import threading
    from http.server import BaseHTTPRequestHandler, HTTPServer

    class HealthHandler(BaseHTTPRequestHandler):
        def do_GET(self):
            self.send_response(200)
            self.send_header('Content-Type', 'text/plain; charset=utf-8')
            self.end_headers()
            self.wfile.write('SmartBot is running'.encode('utf-8'))

        def log_message(self, *args):
            pass  # глушим логи HTTP-запросов

    def _serve():
        server = HTTPServer(('0.0.0.0', int(port)), HealthHandler)
        logger.info(f'Health-сервер слушает порт {port}')
        server.serve_forever()

    threading.Thread(target=_serve, daemon=True).start()


def run_telegram():
    """Запускает Telegram-бота."""
    try:
        from telegram.ext import (
            Updater, CommandHandler, MessageHandler, Filters
        )
        from telegram import Update
    except ImportError:
        print('Установите: pip install python-telegram-bot==13.15')
        return

    # Health-сервер для хостинга (Render Web Service) — слушает PORT, если задан
    start_health_server()

    if TELEGRAM_TOKEN == 'ВАШ_ТОКЕН_ЗДЕСЬ':
        print('Ошибка: укажите TELEGRAM_TOKEN в начале файла!')
        print('Получить токен можно у @BotFather в Telegram.')
        return

    # Увеличенные сетевые таймауты — иначе скачивание голосовых может падать с Timed out
    updater = Updater(
        TELEGRAM_TOKEN,
        request_kwargs={'read_timeout': 30, 'connect_timeout': 30},
    )
