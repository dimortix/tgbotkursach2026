def speech_to_text(audio_path: str):
    """Распознаёт русскую речь из аудиофайла WAV."""
    try:
        import speech_recognition as sr
        recognizer = sr.Recognizer()
        with sr.AudioFile(audio_path) as source:
            audio = recognizer.record(source)
        text = recognizer.recognize_google(audio, language='ru-RU')
        logger.info(f'STT распознал: {text}')
        return text
    except ImportError:
        logger.error('Установите: pip install SpeechRecognition')
        return None
    except Exception as e:
        logger.error(f'Ошибка STT: {e}')
        return None


def text_to_speech(text: str, lang: str = 'ru'):
    """Синтезирует речь из текста, возвращает путь к MP3-файлу."""
    try:
        from gtts import gTTS
        tts = gTTS(text=text, lang=lang, slow=False)
        tmp = tempfile.NamedTemporaryFile(suffix='.mp3', delete=False)
        tmp.close()  # закрываем дескриптор (нужно для Windows-совместимости)
        tts.save(tmp.name)
        return tmp.name
    except ImportError:
        logger.error('Установите: pip install gTTS')
        return None
    except Exception as e:
        logger.error(f'Ошибка TTS: {e}')
        return None

