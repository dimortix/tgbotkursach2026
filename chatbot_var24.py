dialogues_structured_cut = {}

def load_dialogues(path: str = 'dialogues.txt'):
    """Загружает и индексирует датасет диалогов из файла."""
    global dialogues_structured_cut
    if not os.path.exists(path):
        logger.warning(f'Файл {path} не найден. Генеративная модель отключена.')
        logger.warning('Скачайте dialogues.txt и положите рядом со скриптом.')
        return
    with open(path, encoding='utf-8', errors='ignore') as f:
        content = f.read()
    pairs_raw = content.split('\n\n')
    filtered, seen_questions = [], set()
    for block in pairs_raw:
        lines = block.split('\n')[:2]
        if len(lines) != 2:
            continue
        question = clear_phrase(lines[0][2:])
        answer = lines[1][2:]
        if question and question not in seen_questions:
            seen_questions.add(question)
            filtered.append([question, answer])
    # Индексируем по словам для быстрого поиска
    structured = {}
    for q, a in filtered:
        for word in set(q.split()):
            structured.setdefault(word, []).append([q, a])
    # Оставляем не более 1000 пар на слово (по длине вопроса)
    dialogues_structured_cut = {
        w: sorted(pairs, key=lambda p: len(p[0]))[:1000]
        for w, pairs in structured.items()
    }
    logger.info(f'Датасет диалогов загружен: {len(filtered)} пар вопрос-ответ.')

