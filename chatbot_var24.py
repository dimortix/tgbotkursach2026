# РАЗДЕЛ 6: КЛАССИФИКАЦИЯ НАМЕРЕНИЙ С КОНТЕКСТОМ ТЕМ
# ═══════════════════════════════════════════════════════════════════
HIST_THEME_LEN = 10  # максимальная длина истории тем

def classify_intent_by_theme(replica: str, theme=None):
    """
    Классифицирует намерение с учётом текущей темы разговора.
    Использует ML (LinearSVC) + расстояние Левенштейна для проверки.
    """
    replica_clean = clear_phrase(replica)
    if not replica_clean:
        return None

    # ML-предсказание
    predicted = clf.predict(vectorizer.transform([replica_clean]))[0]
    intent_data = BOT_CONFIG['intents'].get(predicted, {})
    theme_app = intent_data.get('theme_app')

    # Проверяем совместимость с текущей темой
    theme_match = (
        (theme_app is not None and (theme in theme_app or '*' in theme_app)) or
        (theme is None and theme_app is None)
    )

    if theme_match:
        # Проверка через расстояние Левенштейна (spell correction)
        for ex in intent_data.get('examples', []):
            ex_clean = clear_phrase(ex)
            if not ex_clean:
                continue
            dist = nltk.edit_distance(replica_clean, ex_clean)
            if dist / len(ex_clean) <= 0.5:
                return predicted

    # Fallback: перебор всех намерений по расстоянию Левенштейна
    for intent, data in BOT_CONFIG['intents'].items():
        ta = data.get('theme_app')
        if not ((ta is not None and (theme in ta or '*' in ta)) or
                (theme is None and ta is None)):
            continue
        for ex in data['examples']:
            ex_clean = clear_phrase(ex)
            if not ex_clean:
                continue
            dist = nltk.edit_distance(replica_clean, ex_clean)
            if dist / len(ex_clean) < 0.4:
                return intent
    return None


def classify_intent(replica: str, hist_theme: list):
    """
    Классифицирует намерение, перебирая историю тем от новой к старой.
    Обновляет hist_theme при смене темы.
    """
    intent = None
    matched_level = 0

    for i, theme in enumerate(hist_theme):
        intent = classify_intent_by_theme(replica, theme)
        if intent:
            matched_level = i
            break

    if intent is None:
        matched_level = 0
        intent = classify_intent_by_theme(replica)
    else:
        if matched_level > 0:
            hist_theme[:] = hist_theme[matched_level:]  # откат к старой теме

    # Обновляем историю тем
    if intent and 'theme_gen' in BOT_CONFIG['intents'][intent]:
        new_theme = BOT_CONFIG['intents'][intent]['theme_gen']
        if new_theme not in hist_theme:
            hist_theme.insert(0, new_theme)
            if len(hist_theme) > HIST_THEME_LEN:
                hist_theme.pop()
    return intent

