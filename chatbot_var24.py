# Счётчики и статистика
_msg_counters = {}
stats = {'intent': 0, 'generate': 0, 'failure': 0, 'total': 0}

# Состояние оформления заказа: user_id -> True, если ждём контактные данные
_user_order_state = {}

# Счётчик оформленных заказов
_order_counter = {'count': 1000}


def maybe_add_ad(user_id, answer: str) -> str:
    """Ненавязчиво добавляет рекламу каждые 5 сообщений."""
    count = _msg_counters.get(user_id, 0) + 1
    _msg_counters[user_id] = count
    if count % 5 == 0:
        answer += random.choice(AD_INSERTIONS)
    return answer


def bot(replica: str, hist_theme: list, user_id='console') -> str:
    """
    Главная функция бота.
    Выполняет NLU → выбор ответа → генерация → fallback.
    """
    stats['total'] += 1

    # 0. Машина состояний заказа: если ждём контактные данные пользователя
    if _user_order_state.get(user_id):
        # Разрешаем отменить оформление
        if clear_phrase(replica) in ('отмена', 'отменить', 'нет', 'не надо', 'стоп'):
            _user_order_state[user_id] = False
            return 'Оформление отменено. Если передумаете — обращайтесь! 😊'
        # Принимаем введённые данные как заказ
        _user_order_state[user_id] = False
        _order_counter['count'] += 1
        order_num = _order_counter['count']
        stats['intent'] += 1
        return (
            f'✅ Заказ №{order_num} успешно оформлен!\n'
            '━━━━━━━━━━━━━━━━━━━\n'
            '📦 Ваши данные приняты:\n'
            f'{replica}\n'
            '━━━━━━━━━━━━━━━━━━━\n'
            '📞 Менеджер свяжется с вами в течение 15 минут для подтверждения.\n'
            '🚚 Доставка — по согласованию.\n\n'
            'Спасибо за покупку! 🎉'
        )

    # Анализ тональности (дополнительная функция)
    sentiment = get_sentiment(replica)

    # 1. NLU: классификация намерения
    intent = classify_intent(replica, hist_theme)

    answer = None

    # 2. Ответ по намерению (заготовленные реплики)
    if intent:
        answer = get_answer_by_intent(intent)
        if answer:
            stats['intent'] += 1
            # Если пользователь решил купить — переходим в режим сбора данных заказа
            if intent == 'buy':
                _user_order_state[user_id] = True

    # 3. Генеративная модель (датасет диалогов)
    if not answer:
        answer = generate_answer(replica)
        if answer:
            stats['generate'] += 1

    # 4. Fallback
    if not answer:
        if sentiment < -0.3:
            # Особый ответ на негативный тон пользователя
            answer = ('😔 Понимаю ваше разочарование. '
                      'Расскажите подробнее — постараемся помочь!')
        else:
            answer = random.choice(BOT_CONFIG['failure_phrases'])
        stats['failure'] += 1

    # 5. Рекламный триггер (сценарий рекламы)
    answer = maybe_add_ad(user_id, answer)

    return answer

